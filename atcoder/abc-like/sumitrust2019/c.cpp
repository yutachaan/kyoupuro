int main() {
  int x; cin >> x;

  int c = x / 100; // 買うことのできる商品の数
  x %= 100;

  // c個以内にの商品を使ってx円をつくれるか
  while (c > 0 and x > 0) {
    if (x >= 5) { x -= 5; c -= 1; }
    else if (x >= 4) { x -= 4; c -= 1; }
    else if (x >= 3) { x -= 3; c -= 1; }
    else if (x >= 2) { x -= 2; c -= 1; }
    else if (x >= 1) { x -= 1; c -= 1; }
  }

  cout << (x <= 0) << endl;

  return 0;
}
